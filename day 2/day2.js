const findOccurance = (arrayOfLetters, number) => {
    let dictionary = {};
    for (let i = 0; i < arrayOfLetters.length; i++) {
        let letter = arrayOfLetters[i];
        dictionary[letter] = dictionary[letter] != null ? dictionary[letter] + 1 : 1;
    }

    let letters = Object.keys(dictionary);
    let occurance = letters.find(letter => dictionary[letter] === number) != null;
    return occurance; 
}
